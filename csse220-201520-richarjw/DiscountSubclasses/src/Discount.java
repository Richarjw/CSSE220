
/**
 * A generic discount interface
 * 
 * @author hewner
 *
 */
public interface Discount {
	/**
	 * Calculates the discount for a given item
	 * 
	 * @param item
	 * @return the savings or 0 if the discount does not apply
	 */
	public double calculateDiscountFor(GroceryItem item);
	
	/**
	 * Returns true if the coupon applies to this particular item
	 * 
	 * @param item
	 * @return true if the coupon applies
	 */
	public boolean matches(GroceryItem item);
}
